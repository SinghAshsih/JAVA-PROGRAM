package com.program.ashish.conversion;

public class IntToDouble {
	public static void main(String[] args) {
		int i = 232324;
		double d = (int) i;
		System.out.println(d);
	}
}

class DoubleToInt {
	public static void main(String[] args) {
		double d = 2334.22122;
		int i = (int) d;
		System.out.println(i);
	}
}